// 游戏者对象
class CPlayer
	{
	public:
		CPlayer() : m_nState(0)
			{
			m_locWorld.x = 223;
			m_locWorld.y = 340;
			m_nWorldDir = 0;
			m_locScene.x = 0;
			m_locScene.y = 0;
			m_nSceneDir = 0;
			}
		~CPlayer() {}

		// 检查游戏者状态
		DWORD Check(DWORD bits) {return CHECK_BIT(m_nState, bits);}


		LOC  m_locWorld;						// 世界地图坐标
		BYTE m_nWorldDir;						// 世界地图朝向
		LOC  m_locScene;						// 场景地图坐标
		BYTE m_nSceneDir;						// 场景地图朝向

	private:
		DWORD m_nState;							// 游戏者状态
	};

